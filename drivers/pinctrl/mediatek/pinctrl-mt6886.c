/* MT6886 have multiple bases to program pin configuration listed as the below:
 * GPIO_BASE: 0x10005000
 * IOCFG_BR_BASE: 0x11C20000
 * IOCFG_LB_BASE: 0x11D10000
 * IOCFG_BM_BASE: 0x11D30000
 * IOCFG_BL_BASE: 0x11D40000
 * IOCFG_LC_BASE: 0x11E10000
 * IOCFG_RC_BASE: 0x11EA0000
 * IOCFG_RT_BASE: 0x11EB0000
 * _i_based could be used to indicate what base the pin should be mapped into.
 */

